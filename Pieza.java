import java.util.*;
public class Pieza {
    int numeroPiezas;

    public Pieza(int numeroPiezas) {
        this.numeroPiezas = numeroPiezas;
    }

    public int getNumeroPiezas() {
        return numeroPiezas;
    }

    public void setNumeroPiezas(int numeroPiezas) {
        this.numeroPiezas = numeroPiezas;
    }
}
